import mongoose from "mongoose";

const userAddressSchema = mongoose.Schema({

    addressLine1: {
        type: String,
        required: true,
    },
    addressLine2: {
        type: String,
    },
    city: {
        type: String,
        required: true,
    },
    state: {
        type: String,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    pincode: {
        type: String,
        required: true,
    }
})

export const UserAddress = mongoose.model("UserAddress", userAddressSchema)